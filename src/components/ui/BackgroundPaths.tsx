function FloatingPaths({ position }: { position: number }) {
  const paths = Array.from({ length: 36 }, (_, i) => {
    const pathLength = 0.3 + (i * 0.02);
    const opacity = 0.1 + (i * 0.03);
    const strokeWidth = 0.5 + (i * 0.03);
    const animationDuration = 20 + Math.random() * 10;
    
    return {
      id: i,
      d: `M-${380 - i * 5 * position} -${189 + i * 6}C-${
        380 - i * 5 * position
      } -${189 + i * 6} -${312 - i * 5 * position} ${216 - i * 6} ${
        152 - i * 5 * position
      } ${343 - i * 6}C${616 - i * 5 * position} ${470 - i * 6} ${
        684 - i * 5 * position
      } ${875 - i * 6} ${684 - i * 5 * position} ${875 - i * 6}`,
      pathLength,
      opacity,
      strokeWidth,
      animationDuration,
    };
  });

  return (
    <div className="absolute inset-0 pointer-events-none overflow-hidden">
      <svg
        className="w-full h-full"
        viewBox="0 0 696 316"
        fill="none"
        xmlns="http://www.w3.org/2000/svg"
      >
        <defs>
          <linearGradient id="fade-gradient" x1="0%" y1="0%" x2="100%" y2="0%">
            <stop offset="0%" stopColor="currentColor" stopOpacity="0" />
            <stop offset="50%" stopColor="currentColor" stopOpacity="1" />
            <stop offset="100%" stopColor="currentColor" stopOpacity="0" />
          </linearGradient>
        </defs>
        <g className="text-slate-900/20 dark:text-white/20">
          {paths.map((path) => (
            <motion.path
              key={path.id}
              d={path.d}
              stroke="url(#fade-gradient)"
              strokeWidth={path.strokeWidth}
              fill="none"
              initial={{ 
                pathLength: 0,
                pathOffset: 1,
                opacity: 0
              }}
              animate={{
                pathLength: [0, 1, 0],
                pathOffset: [1, 0, 1],
                opacity: [0, path.opacity, 0],
              }}
              transition={{
                duration: path.animationDuration,
                repeat: Infinity,
                ease: "linear",
                delay: path.id * 0.1,
              }}
            />
          ))}
        </g>
      </svg>
    </div>
  );
}

